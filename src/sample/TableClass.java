package sample;

public class TableClass {
    private int id_class;
    private int class_code;
    private String class_grade;
    private String class_gradeCode;
    private int id_professor;

    public int getId_class() {
        return id_class;
    }

    public void setId_class(int id_class) {
        this.id_class = id_class;
    }

    public int getClass_code() {
        return class_code;
    }

    public void setClass_code(int class_code) {
        this.class_code = class_code;
    }

    public String getClass_grade() {
        return class_grade;
    }

    public void setClass_grade(String class_grade) {
        this.class_grade = class_grade;
    }

    public String getClass_gradeCode() {
        return class_gradeCode;
    }

    public void setClass_gradeCode(String class_gradeCode) {
        this.class_gradeCode = class_gradeCode;
    }

    public int getId_professor() {
        return id_professor;
    }

    public void setId_professor(int id_professor) {
        this.id_professor = id_professor;
    }
}
